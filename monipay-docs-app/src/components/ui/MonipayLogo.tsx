// SVG viewport (original logo coordinates)
const VIEWBOX = "178 224 1159 1056";

export interface MonipayLogoProps {
  size?: number;
  color?: string;
  animationMode?: 'idle' | 'splash' | 'header' | 'processing' | 'success' | 'error';
  isError?: boolean;
  isEmpty?: boolean;
  showText?: boolean;
  textSize?: number;
  entranceOnMount?: boolean;
  entranceOnView?: boolean;
  onSplashComplete?: () => void;
}

export function MonipayLogo({
  size = 40,
  color = '#0052FF',
  animationMode = 'idle',
  isError = false,
  isEmpty = false,
  showText = false,
  textSize,
  entranceOnMount = false,
  entranceOnView = false,
  onSplashComplete,
}: MonipayLogoProps) {
  const [dotPulse, setDotPulse] = useState(false);
  const [hasAnimated, setHasAnimated] = useState(false);
  const dotControls = useAnimation();
  const prevIsError = useRef(false);

  // ── InView ref for footer entrance
  const containerRef = useRef<HTMLDivElement>(null);
  const inView = useInView(containerRef, { once: true, margin: '-40px' });

  const shouldEntrance =
    (entranceOnMount && !hasAnimated) ||
    (entranceOnView && inView && !hasAnimated);

  const mControls = useAnimation();
  const strokeControls = useAnimation();
  const dotEntranceControls = useAnimation();

  useEffect(() => {
    if (!shouldEntrance) return;
    setHasAnimated(true);
    strokeControls.start({ pathLength: 1, transition: { duration: 1.0, ease: 'easeInOut' } });
    setTimeout(() => {
      strokeControls.start({ opacity: 0, transition: { duration: 0.3 } });
    }, 1100);
    dotEntranceControls.start({
      scale: 1,
      opacity: 1,
      transition: { delay: 0.1, type: 'spring', stiffness: 260, damping: 20 },
    });
  }, [shouldEntrance, strokeControls, dotEntranceControls]);

  // ── Header mode: 30-second dot heartbeat
  useEffect(() => {
    if (animationMode !== 'header') return;
    const id = setInterval(() => {
      setDotPulse(true);
      setTimeout(() => setDotPulse(false), 600);
    }, 30000);
    return () => clearInterval(id);
  }, [animationMode]);

  // ── Error shake
  useEffect(() => {
    if (isError && !prevIsError.current) {
      dotControls.start({
        x: [0, -8, 8, -8, 8, -4, 4, 0],
        transition: { duration: 0.4, ease: 'easeInOut' },
      });
    }
    prevIsError.current = isError;
  }, [isError, dotControls]);

  // ── Splash: fire onSplashComplete after animation (2s draw + 0.4s delay + ~0.8s spring settle)
  useEffect(() => {
    if (animationMode === 'splash' && onSplashComplete) {
      const t = setTimeout(onSplashComplete, 4200);
      return () => clearTimeout(t);
    }
  }, [animationMode, onSplashComplete]);

  const isSplash = animationMode === 'splash';
  const isProcessing = animationMode === 'processing';
  const isSuccess = animationMode === 'success';
  const dotFill = isSuccess ? '#22c55e' : color;
  const successColor = '#22c55e';
  const computedTextSize = textSize ?? Math.round(size * 0.45);
  const isEntrance = entranceOnMount || entranceOnView;

  return (
    <div
      ref={containerRef}
      style={{ display: 'inline-flex', alignItems: 'center', gap: Math.round(size * 0.1) }}
      aria-label="Monipay logo"
    >
      <div style={{ width: size, height: size, position: 'relative', flexShrink: 0 }}>
        <svg
          viewBox={VIEWBOX}
          xmlns="http://www.w3.org/2000/svg"
          style={{ width: '100%', height: '100%', overflow: 'visible' }}
        >
          {/* ── M letterform ── */}
          <motion.path
            d={M_PATH}
            fill={isSplash ? 'none' : color}
            animate={mControls}
            style={{ opacity: isProcessing ? 0.8 : 1 }}
            transition={{ duration: 0.4 }}
          />

          {/* Entrance / Splash: stroke-draw overlay */}
          {(isSplash || isEntrance) && (
            <motion.path
              d={M_PATH}
              fill="none"
              stroke={color}
              strokeWidth={60}
              strokeLinecap="round"
              strokeLinejoin="round"
              initial={{ pathLength: 0, opacity: 1 }}
              animate={
                isSplash
                  ? { pathLength: 1 }
                  : strokeControls
              }
              transition={
              isSplash
                   ? { duration: 2.8, ease: 'easeInOut' }
                  : undefined
              }
            />
          )}

          {/* ── Dot ── */}
          <motion.g animate={dotControls}>
            {isSuccess ? (
              <motion.path
                d={DOT_PATH}
                fill={successColor}
                initial={{ scale: 0.8, opacity: 0 }}
                animate={{ scale: [1, 1.2, 1], opacity: 1 }}
                style={{ transformOrigin: '1222px 1031px' }}
                transition={{ repeat: Infinity, duration: 1.5, ease: 'easeInOut' }}
              />
            ) : (
              <motion.path
                d={DOT_PATH}
                fill={dotFill}
                stroke={undefined}
                strokeWidth={0}
                initial={
                  isSplash || isEntrance
                    ? { scale: 0, opacity: 0 }
                    : undefined
                }
                animate={
                  isSplash
                    ? { scale: 1, opacity: 1 }
                    : isEntrance
                    ? dotEntranceControls
                    : isProcessing
                    ? { scale: [1, 1.4, 1], opacity: [1, 0.5, 1] }
                    : animationMode === 'header' && dotPulse
                    ? { scale: [1, 1.3, 1] }
                    : { fill: dotFill, scale: 1, opacity: 1 }
                }
                style={{ transformOrigin: '1222px 1031px' }}
                transition={
                  isSplash
                    ? { delay: 3.2, type: 'spring', stiffness: 260, damping: 20 }
                    : isProcessing
                    ? { repeat: Infinity, duration: 1, ease: 'easeInOut' }
                    : animationMode === 'header' && dotPulse
                    ? { type: 'spring', stiffness: 300, damping: 20 }
                    : { duration: 0.6, ease: 'easeInOut' }
                }
              />
            )}
          </motion.g>
        </svg>
      </div>

      {/* ── Wordmark: "Moni" (foreground) + "pay" (brand color) ── */}
      {showText && (
        <motion.span
          initial={isEntrance ? { opacity: 0, x: -6 } : { opacity: 1, x: 0 }}
          animate={{ opacity: 1, x: 0 }}
          transition={{ delay: isEntrance ? 0.5 : 0, duration: 0.4, ease: 'easeOut' }}
          style={{
            fontSize: computedTextSize,
            fontWeight: 800,
            letterSpacing: '-0.02em',
            lineHeight: 1,
            userSelect: 'none',
            display: 'inline-flex',
            alignItems: 'baseline',
          }}
        >
          <span className="receipt-moni-text" style={{ color: 'hsl(var(--foreground))' }}>Moni</span>
          <span style={{ color: color }}>pay</span>
        </motion.span>
      )}
    </div>
  );
}
